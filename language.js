function setLanguage(lang) {
  // Basic text
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    el.textContent = translations[lang][key] || key;
  });

  // Placeholder
  document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
    const key = el.getAttribute("data-i18n-placeholder");
    el.setAttribute("placeholder", translations[lang][key] || key);
  });

  // Aria-label
  document.querySelectorAll("[data-i18n-aria]").forEach(el => {
    const key = el.getAttribute("data-i18n-aria");
    el.setAttribute("aria-label", translations[lang][key] || key);
  });

  // HTML (for rich content like spans)
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.getAttribute("data-i18n-html");
    el.innerHTML = translations[lang][key] || key;
  });

  localStorage.setItem("lang", lang);
}

window.addEventListener("DOMContentLoaded", () => {
  const lang = localStorage.getItem("lang") || "en";
  setLanguage(lang);

  const selector1 = document.getElementById("language-selector");
  const selector2 = document.getElementById("language-selector-mobile");
  const selector3 = document.getElementById("language-selector-employee");

  if (selector1) {
    selector1.value = lang;
    selector1.addEventListener("change", e => {
      setLanguage(e.target.value);
      // Sync other selectors
      if (selector2) selector2.value = e.target.value;
      if (selector3) selector3.value = e.target.value;
    });
  }

  if (selector2) {
    selector2.value = lang;
    selector2.addEventListener("change", e => {
      setLanguage(e.target.value);
      // Sync other selectors
      if (selector1) selector1.value = e.target.value;
      if (selector3) selector3.value = e.target.value;
    });
  }

  if (selector3) {
    selector3.value = lang;
    selector3.addEventListener("change", e => {
      setLanguage(e.target.value);
      // Sync other selectors
      if (selector1) selector1.value = e.target.value;
      if (selector2) selector2.value = e.target.value;
    });
  }
});
